"""
dataset.py — HDF5-backed protein dataset for training.

Public API
----------
ProteinDataset(h5_path)          : random-access dataset over the HDF5 file
get_training_pair(pid, ...)      : build (noisy_coords, true_coords, esm2) triple

HDF5 schema expected (written by preprocess_dataset.py):
  /proteins/{pid}/sequence     — bytes
  /proteins/{pid}/true_coords  — (L, 4, 3) float32
  /proteins/{pid}/phi_psi      — (L, 2) float32, degrees, NaN at terminals
  /proteins/{pid}/esm2         — (L, 320) float32
"""

import numpy as np
import torch
import h5py

from pipeline.nerf import build_backbone
from pipeline.geometry import center_coords, kabsch_align


class ProteinDataset:
    """Thin wrapper around an HDF5 protein dataset.

    Provides random access to per-protein data and a lazy open/close
    pattern so the file handle stays open for the lifetime of the object.

    Parameters
    ----------
    h5_path  : str  — path to the HDF5 file produced by preprocess_dataset.py
    min_len  : int  — skip proteins shorter than this
    max_len  : int  — skip proteins longer than this (pos-emb table limit)
    """

    def __init__(self, h5_path: str, min_len: int = 5, max_len: int = 2048):
        self.h5_path = h5_path
        self.min_len = min_len
        self.max_len = max_len
        self._hf = h5py.File(h5_path, 'r')
        self._grp = self._hf['proteins']
        all_pids = sorted(self._grp.keys())

        # Pre-filter by length using the phi_psi shape (fast — no data read)
        self.pids = [
            pid for pid in all_pids
            if min_len <= self._grp[pid]['phi_psi'].shape[0] <= max_len
        ]

    def __len__(self):
        return len(self.pids)

    def __contains__(self, pid: str):
        return pid in self._grp

    def get_sequence(self, pid: str) -> str:
        return self._grp[pid]['sequence'][()].decode('ascii')

    def get_true_coords(self, pid: str) -> np.ndarray:
        """Return (L, 4, 3) float32 backbone coordinates."""
        return self._grp[pid]['true_coords'][()]

    def get_phi_psi(self, pid: str) -> np.ndarray:
        """Return (L, 2) float32 torsion angles in degrees (NaN at terminals)."""
        return self._grp[pid]['phi_psi'][()]

    def get_esm2(self, pid: str) -> np.ndarray:
        """Return (L, 320) float32 ESM-2 hidden states."""
        return self._grp[pid]['esm2'][()]

    def close(self):
        self._hf.close()

    def __del__(self):
        try:
            self._hf.close()
        except Exception:
            pass


# ── Training pair builder ──────────────────────────────────────────────────────

@torch.no_grad()
def get_training_pair(
    pid:      str,
    dataset:  ProteinDataset,
    noise_std: float = 25.0,
    flow_model=None,
):
    """Build a (noisy_coords, true_coords, esm2) training triple.

    Noisy coordinates are generated by:
      - If flow_model is provided: run the flow ODE on the full protein
      - Otherwise: add Gaussian noise (σ = noise_std°) to the true φ/ψ angles

    Both coordinate sets are centred at their Cα centroid; noisy is then
    Kabsch-aligned to true to correct the arbitrary NeRF frame.

    Parameters
    ----------
    pid        : str   — protein identifier
    dataset    : ProteinDataset
    noise_std  : float — Gaussian angle noise in degrees (ignored if flow_model given)
    flow_model : ConditionedFlow or None

    Returns
    -------
    (noisy_t, true_t, esm2_t) : tuple[torch.Tensor] or None on failure
      noisy_t : (L, 12) float32 — flattened [N,CA,C,O]×xyz, aligned to true
      true_t  : (L, 12) float32 — centred PDB coordinates
      esm2_t  : (L, 320) float32
    """
    if pid not in dataset:
        return None

    sequence   = dataset.get_sequence(pid)
    true_raw   = dataset.get_true_coords(pid).astype(np.float64)   # (L, 4, 3)
    phi_psi_db = dataset.get_phi_psi(pid)                          # (L, 2) deg
    esm2_emb   = dataset.get_esm2(pid)                             # (L, 320)

    L = len(sequence)
    if len(true_raw) != L or len(esm2_emb) != L or L < 5:
        return None

    # ── True coordinates: centre ──────────────────────────────────────────────
    true_c, _ = center_coords(true_raw)

    # ── Noisy φ/ψ angles ─────────────────────────────────────────────────────
    if flow_model is not None:
        flow_model.eval()
        emb_t = torch.tensor(esm2_emb, dtype=torch.float32)
        x     = torch.randn(L, 2)
        ts    = torch.linspace(0., 1., 21)
        for i in range(20):
            t_s = ts[i].view(1, 1).expand(L, 1)
            dt  = (ts[i + 1] - ts[i]).float()
            v_s = flow_model(t_s, x, emb_t)
            v_m = flow_model(t_s + dt / 2, x + v_s * dt / 2, emb_t)
            x   = x + dt * v_m
        phi_psi = x.numpy() * 180.0                                # (L, 2)
    else:
        # Replace NaN terminals with canonical α-helix defaults (-60°, -40°)
        phi_psi = phi_psi_db.copy()
        nan_mask = np.isnan(phi_psi)
        phi_psi[nan_mask[:, 0], 0] = -60.0
        phi_psi[nan_mask[:, 1], 1] = -40.0
        phi_psi = phi_psi + np.random.randn(L, 2).astype(np.float32) * noise_std

    # ── NeRF → centre → Kabsch-align ─────────────────────────────────────────
    noisy_raw  = build_backbone(sequence, phi_psi)
    noisy_c, _ = center_coords(noisy_raw)
    noisy_aln  = kabsch_align(noisy_c, true_c)

    return (
        torch.tensor(noisy_aln.reshape(L, 12), dtype=torch.float32),
        torch.tensor(true_c.reshape(L, 12),    dtype=torch.float32),
        torch.tensor(esm2_emb,                  dtype=torch.float32),
    )


# ── Flow model training data loader ───────────────────────────────────────────

def build_flow_tensors(dataset: ProteinDataset):
    """Build flat (N_interior, 2) angle and (N_interior, 320) embedding tensors
    for flow model training from the HDF5 dataset.

    Interior residues only (rows where neither phi nor psi is NaN).

    Returns
    -------
    angles_tensor : torch.Tensor (N, 2)    — normalised to [-1, 1]
    esm2_tensor   : torch.Tensor (N, 320)
    pdb_ids       : list[str]              — protein ID per row
    """
    angle_rows, esm2_rows, pid_rows = [], [], []

    for pid in dataset.pids:
        phi_psi = dataset.get_phi_psi(pid)    # (L, 2)
        esm2    = dataset.get_esm2(pid)        # (L, 320)

        # Interior residues: both phi and psi are defined (not NaN)
        interior = ~np.any(np.isnan(phi_psi), axis=1)
        if not interior.any():
            continue

        angle_rows.append(phi_psi[interior] / 180.0)  # normalise to [-1, 1]
        esm2_rows.append(esm2[interior])
        pid_rows.extend([pid] * int(interior.sum()))

    angles_tensor = torch.tensor(np.concatenate(angle_rows), dtype=torch.float32)
    esm2_tensor   = torch.tensor(np.concatenate(esm2_rows),  dtype=torch.float32)
    return angles_tensor, esm2_tensor, pid_rows
